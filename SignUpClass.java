    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!checker(password.getText())){
            JOptionPane.showMessageDialog(null,"The password must contain a capital letter, a small letter, a number and a special character");
        }else{
            String n=name.getText();
            String e=enrollmentNumber.getText();
            String s=school.getText();
            String b=branch.getText();
            String sp=special.getText();
            String nu=number.getText();
            String em=email.getText();
            String pass=password.getText();

            StudentInfo temp = new StudentInfo(n,e,s,b,sp,nu,em,pass);
            al.add(temp);
            back.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

